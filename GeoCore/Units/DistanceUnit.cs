namespace GeoCore.Units
{
    /// <summary>
    /// Represents the supported units of distance measurement.
    /// </summary>
    public enum DistanceUnit
    {
        /// <summary>
        /// Kilometers (1 km = 1,000 meters).
        /// </summary>
        Kilometers,

        /// <summary>
        /// Meters.
        /// </summary>
        Meters,

        /// <summary>
        /// Statute miles (1 mile = 1.60934 kilometers).
        /// </summary>
        Miles,

        /// <summary>
        /// Nautical miles (1 NM = 1.852 kilometers).
        /// </summary>
        NauticalMiles,

        /// <summary>
        /// Feet (1 foot = 0.3048 meters).
        /// </summary>
        Feet,

        /// <summary>
        /// Yards (1 yard = 0.9144 meters).
        /// </summary>
        Yards
    }
}
